
public class Person {
	public String name;
    public double gpa;
    public double tall;
    
    public void setName(String personName) {
        this.name = personName;
    }
    public void setGpa(double personGpa) {
        this.gpa = personGpa;
    }
    public void setTall(double personTall) {
        this.tall = personTall;
    }

    public String getName() {
        return this.name;
    }
    public double getGpa() {
        return this.gpa;
    }
    public double getTall() {
        return this.tall;
    }

}
